void keyboard_post_init_user(void) {
    rgb_matrix_mode_noeeprom(RGB_MATRIX_NONE);
    rgb_matrix_sethsv_noeeprom(HSV_WHITE);
}

// set hue/saturation from colormap, value from current setting
// uses value in colormap to determine if LED should be illuminated or not
void set_layer_rgb(int layer) {
  const ledmap *l = &(ledmaps[layer]);

  uint8_t val = rgb_matrix_get_val();

  for (int i = 0; i < 12; i++) {
    HSV hsv = {
      .h = (*l)[i][0],
      .s = (*l)[i][1],
      .v = val,
    };

    if (hsv.h || hsv.s) {
      RGB rgb = hsv_to_rgb(hsv);
      rgb_matrix_set_color(i, rgb.r, rgb.g, rgb.b);
    }
  }
}

bool rgb_matrix_indicators_user(void){
  rgb_matrix_set_color_all(RGB_OFF);
  set_layer_rgb(get_highest_layer(layer_state | default_layer_state));
  return false;
}

// Generate Keypresses at some interval

bool process_record_user(uint16_t keycode, keyrecord_t* record) {
    switch (keycode) {
        case KEY_TIMER:
            if (record->event.pressed) {
                key_trigger ^= true;
            }
            break;
    }
    return true;
}

void matrix_scan_user(void) { 
    if (timer_elapsed32(key_timer) > 30000) { // 30 seconds
        key_timer = timer_read32();  // resets timer
        if (key_trigger) tap_code(KC_F20); // tap if enabled
    }
}

